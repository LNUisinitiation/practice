package com.lnu.java;

public class KY2_1{
	public static void main(String[] args) {
		byte b = 0x55; //ox是多少进制
		short s = 0x55ff; //为什么后面加ff
		int i = 1000000000;
		long l = 0xffffL; //加L或l
		char c = 'a';
		float f = 0.23F; //加F或f
		double d = 0.7E3; //0.7E-3 为什么加- 小写e可以吗
		boolean bool = true;
		String str = "这是字符串类数据类型";
		System.out.println("字节型变量 b = " + b);
		System.out.println("短整型变量 s = " + s);
		System.out.println("整型变量 i = " + i);
		System.out.println("长整型变量 l = " + l);
		System.out.println("字符型变量 c = " + c);
		System.out.println("浮点型变量 f = " + f);
		System.out.println("双精度浮点型变量 d = " + d);
		System.out.println("布尔型变量 bool = " + bool);
		System.out.println("字符串类对象 str = " + str);
		
	}
}























































